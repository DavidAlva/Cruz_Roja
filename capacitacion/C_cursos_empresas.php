<h1>INTRODUCCIÓN</h1> 

<p>El 80% de los accidentes, son provocados por el hombre, la mayoría de los mismos, son reconocidos y  previsibles, antes de que  se presenten.</p>

<p>En un accidente confluyen varios factores para que este se dé y que pudieron haberse evitado, desgraciadamente cuando el accidente sucede, pocas son las personas que saben actuar ante una emergencia industrial o familiar.</p>

<p>Muchas discapacidades y vidas pudieron haberse salvado, de haber sabido que hacer y que no hacer ante una emergencia, en los minutos posteriores a su presentación, ya que muchas veces el hacer un movimiento inadecuado en un lesionado, puede representar una incapacidad permanente y total y algunas veces la vida misma.</p>

<p>Cruz Roja Mexicana cuenta con una muy amplia experiencia en el manejo de todo tipo de pacientes, sean estos por enfermedad súbita o por accidente.</p>

