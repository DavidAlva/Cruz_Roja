<h3>5. Disposiciones generales</h3>

<p>5.1. De las ambulancias en general:</p>
<p>5.1.1. Deben ser utilizadas únicamente para el propósito que hayan sido autorizadas y queda prohibido
transportar o almacenar cualquier material que ponga en peligro la vida o salud del paciente y del personal que
preste el servicio.</p>
<p>5.1.2. Deberán cumplir con las disposiciones en la materia, para la utilización del equipo de seguridad y
protección del paciente y personal que proporcione los servicios.</p>
<p>5.1.2.1. Deberán cumplir con las disposiciones en la materia, para el manejo de los residuos peligrosos
biológico-infecciosos.</p>
<p>5.1.2.2. Deberá recibir capacitación periódica, todo personal que preste servicios de salud a bordo de una
ambulancia, atendiendo a su denominación y nivel resolutivo.</p>
<p>5.1.3. Para garantizar condiciones adecuadas de funcionamiento y seguridad, el vehículo y el equipo deberán
recibir mantenimiento periódico, de acuerdo con los lineamientos establecidos por las autoridades competentes.</p>
<p>5.1.4. Deben apegarse a la reglamentación que establezca la Federación y entidades federativas en relación al
tránsito, control de emisión de contaminantes, uso de mar territorial y espacio aéreo.</p>
<p>5.1.5. Deberán participar, bajo la coordinación de las autoridades que corresponda, en las tareas de atención de
incidentes con múltiples víctimas y en los casos de desastre.</p>
<p>5.1.6. Deberán portar al frente, en los costados y en la parte posterior la leyenda “AMBULANCIA”, en la parte
frontal su imagen deberá ser en espejo, es decir, “invertida”, en material reflejante y en color contrastante con el
vehículo, con letras de tamaño no menor a 10 centímetros; además en los costados se especificará el tipo de
ambulancia de que se trate: traslado, urgencias básicas o avanzadas o, cuidados intensivos, en su caso, deberán
rotularse toldo, cubierta y fuselaje. La cabina para el paciente deberá contar con vidrios que impidan la visibilidad
desde el exterior, pueden ser polarizados, entintados, esmerilados, opacos u otros. Excepción hecha para las
ambulancias de las fuerzas armadas.</p>
<p>5.1.7. Deberán contar con un rótulo en donde se especifique la institución a la que pertenecen o razón social y el
número económico de la unidad, en material reflejante y en color contrastante con el vehículo, ubicado en los
costados y en la parte posterior de la unidad, con caracteres de tamaño no menor a 8 centímetros y en el toldo del
vehículo con caracteres de tamaño no menor a 40 centímetros.</p>
<p>5.2. De las ambulancias terrestres
(De traslado, urgencias básicas o avanzadas y cuidados intensivos)</p>
<p>5.2.1. Deberán contar con dos lámparas que emitan luces rojas y blancas hacia adelante, de manera intermitente
y una torreta con lámparas giratorias de 360 grados o estroboscópicas o intermitentes que proyecten luz roja,
visibles desde una distancia de 150 metros.</p>
<p>5.2.1.1. Deberán contar con una sirena mecánica o electrónica, que genere sonidos de 124 decibeles en
promedio.</p>
<p>5.2.1.2. El uso de la sirena y las luces de emergencia se limitará estrictamente a la necesidad de solicitar paso
preferente al acudir al llamado de una urgencia o durante el traslado de un paciente en estado grave o crítico. Las
luces de emergencia, podrán emplearse de manera independiente, con o sin el uso de la sirena siempre que exista
un paciente a bordo de la ambulancia, dependiendo de su condición o estado de salud.</p>
<p>5.2.2. Deberán contar con un compartimiento de atención, el cual deberá ser diseñado para tener espacio libre,
que de cabida al menos a un paciente en carro camilla y a dos integrantes de la tripulación que lo atiendan y que
puedan ser trasladados sentados.</p>
<p>5.2.2.1. El compartimiento de atención, debe contar con un sistema de iluminación con suficiente intensidad para
permitir la evaluación del paciente y la identificación apropiada de los materiales y suministros que se requieran para
la atención del mismo.</p>
<p>5.2.3. En el compartimiento de atención del paciente, las ambulancias de urgencias y cuidados intensivos,
deberán tener como mínimo 1.60 metros de altura, 1.90 metros de ancho y 2.50 metros de largo.</p>
<p>5.2.3.1. Las ambulancias de traslado deberán tener como mínimo, 1.35 metros de altura, 1.50 metros de ancho y
2.00 metros de largo.</p>
<p>5.2.4. Deberán contener los recursos físicos de apoyo, equipo médico, suministros y medicamentos
especificados en los apéndices normativos, según corresponda a cada tipo de ambulancia.</p>
<p>5.3. De las ambulancias aéreas y marítimas
53 (Primera Sección) DIARIO OFICIAL Jueves 15 de junio de 2006</p>
<p>5.3.1. Deben estar configuradas de acuerdo a las especificaciones de diseño del fabricante y contar con un área
de cuidado que permita la atención del paciente durante su traslado, así como espacio suficiente para acomodar
cuando menos, a un médico o técnico en urgencias médicas o personal de enfermería capacitado.</p>
<p>5.4. Del equipamiento
Las ambulancias deberán observar los contenidos especificados en los numerales de los apéndices normativos
que correspondan al tipo de ambulancia de que se trate, cuidando observar las características particulares del
equipo que debe emplearse en condiciones de vuelo.</p>
<p>5.5. De los suministros
Las ambulancias deberán observar los contenidos especificados en los numerales de los apéndices normativos
que correspondan al tipo de ambulancia de que se trate.</p>
<p>5.6. Del personal</p>
<p>5.6.1. En ambulancias de traslado.</p>
<p>5.6.1.1. Deben contar con un operador de ambulancia que demuestre documentalmente haber acreditado
satisfactoriamente cursos afines a la atención prehospitalaria de las urgencias médicas.</p>
<p>5.6.2. En ambulancias de urgencias básicas o avanzadas.</p>
<p>5.6.2.1. Deben contar con un operador de ambulancia que demuestre documentalmente haber acreditado
satisfactoriamente cursos afines a la atención prehospitalaria de las urgencias médicas y al menos un técnico en
urgencias médicas con diploma legalmente expedido y registrado por las autoridades educativas competentes.</p>
<p>5.6.3. En ambulancias de cuidados intensivos.</p>
<p>5.6.3.1. Debe contar con un operador de ambulancia que demuestre documentalmente haber acreditado cursos
de atención prehospitalaria, un recurso de enfermería o un técnico en urgencias médicas, que demuestren
documentalmente haber acreditado cursos de atención prehospitalaria, manejo de pacientes en estado crítico y
cuidados intensivos, avalados por Instituciones reconocidas.</p>
<p>5.6.3.2. Debe contar con un médico especialista con capacitación en atención prehospitalaria, manejo de
pacientes en estado crítico y cuidados intensivos.</p>
<p>5.6.4. En ambulancias aéreas.</p>
<p>5.6.4.1. Deberá contar con un piloto aviador que deberá cumplir con lo que establezca la Dirección General de
Aeronáutica Civil, a excepción de las ambulancias de las fuerzas armadas.</p>
<p>5.6.4.2. Debe contar con un técnico en urgencias médicas o un recurso de enfermería con capacitación en
atención prehospitalaria de las urgencias médicas, con diploma expedido por autoridad educativa competente, en
ambos casos es necesario que demuestren documentalmente haber acreditado cursos de medicina aeroespacial.</p>
<p>5.6.4.3. En el caso que se proporcione servicio de cuidados intensivos, deberá contar con un médico especialista
que demuestre documentalmente haber acreditado satisfactoriamente cursos de manejo del paciente en estado
crítico y cuidados intensivos. En ambos casos, se requiere además tener conocimientos de medicina aeroespacial.</p>
<p>5.7. De la atención de las urgencias prehospitalarias.</p>
<p>5.7.1. Del sitio de la urgencia médica.</p>
<p>5.7.1.1. La atención médica se brindará en áreas geográficas determinadas por el CRUM, conforme lo indiquen
los criterios de regionalización, isócronas de traslado de la base de ambulancias al sitio de la urgencia médica.</p>
<p>5.7.1.2. La solicitud de atención prehospitalaria se hará directamente al CRUM a través de un número telefónico
único, gratuito de tres dígitos y preferentemente medicalizado. Este organismo se enlazará con los establecimientos
para la atención médica fijos y móviles, por medio de un sistema de radiocomunicación que pueda acceder a las
frecuencias de las instancias involucradas o a través de cualquier otro sistema de comunicación que resulte
conveniente para los fines de coordinación.</p>
<p>5.7.1.3. Toda llamada de auxilio recibida en el CRUM, deberá ser atendida, tipificada, clasificada, registrada y se
llevará a cabo el seguimiento correspondiente.</p>
<p>5.7.1.4. La coordinación logística y operativa, así como la asesoría en la atención prehospitalaria de las
urgencias médicas, será proporcionada por el personal operativo del CRUM en turno, que en todos los casos,
deberá estar integrado por un médico y técnicos en urgencias médicas, que demuestren documentalmente haber
acreditado satisfactoriamente cursos de atención prehospitalaria de las urgencias médicas en Instituciones
reconocidas.
Jueves 15 de junio de 2006 DIARIO OFICIAL (Primera Sección) 54</p>
<p>5.7.1.5. El CRUM enviará al sitio de la urgencia a la ambulancia disponible más adecuada que se encuentre más
cercana, para brindar la atención inmediata y apropiada y, de acuerdo a la gravedad del caso, coordinará el traslado
al servicio de urgencias del establecimiento para la atención médica, que resulte más conveniente.</p>
<p>5.7.1.6. El TUM o el personal responsable de la atención en la ambulancia, reportará al CRUM los hallazgos
clínicos y en su caso, el diagnóstico presuncional, el estado psicofísico del paciente, así como las necesidades
inmediatas que requieren ser preparadas en el establecimiento para la atención médica al que se dirige; todos estos
eventos deben quedar asentados en un formato para el registro de la atención prehospitalaria de las urgencias
médicas.</p>
<p>5.7.1.7. El manejo de la atención prehospitalaria deberá realizarse de acuerdo a los protocolos escritos, que para
la naturaleza del evento tenga definidos la institución responsable de brindar la atención prehospitalaria. Los
contenidos podrán diferir por cada institución, de acuerdo a la lex artis médica. En todos los casos, los protocolos
deberán estar avalados y firmados por la autoridad médica o el responsable sanitario del servicio de ambulancias.</p>
<p>5.7.1.8. Los formatos para el registro de la atención prehospitalaria de las urgencias médicas,
independientemente del diseño que cada institución desarrolle, deberán contener como mínimo, los siguientes
campos para el registro de información:</p>
<ol>
<li>Datos de registro: Número progresivo del folio del formato.</li>
<li>Fecha del servicio: Día, mes y año.</li>
<li>Identificación de la ambulancia: Número económico, placas e Institución a la que pertenece.</li>
<li>Tipo de servicio: Traslado, urgencia o cuidados intensivos.</li>
<li>Lugar de ocurrencia de la urgencia: Hogar, escuela, trabajo, instalaciones deportivas, de recreación o vía
pública u otras.</li>
<li>Hora de salida de la ambulancia de la base; hora de primer contacto.</li>
<li>Hora de fin de la atención o alta del paciente: En el sitio de la urgencia o de la recepción del paciente en el
establecimiento para la atención médica.</li>
<li>Identificación del personal operativo: Operador de la unidad, TUM u otros prestadores de servicios de
salud.</li>
<li>Datos del paciente: Nombre, edad y sexo o media filiación</li>
<li>Antecedentes personales patológicos.</li>
<li>Padecimiento actual: Causa traumática o no traumática de la urgencia. Descripción del mecanismo de
lesión, enfermedad súbita, enfermedad crónica o complicación de enfermedad crónica, según proceda.</li>
<li>Exploración física básica: Signos vitales, estado y coloración de la piel y estado de pupilas.</li>
<li>Descripción de lesiones o afecciones: Localización, tipo de afectación a órganos, aparatos o sistemas.</li>
<li>Manejo proporcionado: Vía aérea, ventilación, circulación, líquidos endovenosos, fármacos, inmovilización,
así como cualquier otra observación.</li>
<li>Derivación del paciente: Establecimiento para la atención médica, el hogar o alta en el sitio; cuando
aplique, datos y firmas de los responsables que entregan y reciben al paciente.</li>
</ol>

<p>5.7.2. Del traslado del paciente al servicio de urgencias en establecimientos para la atención médica.</p>
<p>5.7.2.1. En caso necesario, el CRUM brindará asesoría, apoyo médico y asistencia en la aplicación de protocolos
para el manejo de pacientes graves a las ambulancias que lo requieran, por medio de sistemas de comunicación de
radio o cualquier otro medio que resulte apropiado.</p>
<p>5.7.2.2. Los traslados dependerán de la regionalización del CRUM, de la causa del evento crítico del paciente, de
la ubicación, disponibilidad y capacidad resolutiva, tanto del establecimiento para la atención médica, como de las
ambulancias, así como de las rutas e isócronas de traslado.</p>
<p>5.7.2.3. El CRUM deberá dar aviso con oportunidad al establecimiento para la atención médica sobre la
posibilidad de traslado del paciente que recibe atención prehospitalaria en una ambulancia, para que se decida,
previa valoración del caso, su ingreso y tratamiento inmediato o en su defecto, el traslado a otro establecimiento con
mayor capacidad resolutiva.</p>
<p>5.7.2.4. El TUM o el personal responsable que atendió y estuvo a cargo del traslado del paciente, deberá
consignar en un formato para el registro de atención prehospitalaria, todos los eventos ocurridos con motivo de la
atención de un paciente, debiendo considerar desde que la ambulancia acudió al llamado, hasta el momento en que
el paciente es entregado en un establecimiento para la atención médica, es dado de alta en el lugar del suceso u
otro sitio de finalización del traslado.
55 (Primera Sección) DIARIO OFICIAL Jueves 15 de junio de 2006</p>
<p>5.7.2.5. El personal médico o paramédico de la ambulancia que lleve a cabo el traslado, es responsable del
paciente durante el mismo, toda vez que es considerada un establecimiento para la atención médica.</p>
<p>5.7.3. De la recepción del paciente en el establecimiento para la atención médica.</p>
<p>5.7.3.1. El personal del establecimiento para la atención médica o de la ambulancia en su caso, dará aviso al
ministerio público cuando se presuma que se trata de un caso médico legal.</p>
<p>5.7.3.2. Conforme a un formato que diseñe cada Institución, se cotejarán, recibirán y aceptarán las pertenencias
que fueron recibidas por el personal del establecimiento para la atención médica a que fue referido el paciente.</p>
<p>5.7.3.3. En el expediente clínico, deberá integrarse una copia del formato de registro de la atención
prehospitalaria, que el personal de la ambulancia debe entregar en el establecimiento para la atención médica a que
fue referido el paciente.</p>
