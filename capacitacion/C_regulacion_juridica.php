<div class="columnas">
<h3>4. Definiciones</h3>
<p>Para efectos de esta Norma Oficial Mexicana, se entiende por:</p>
<p>4.1. Ambulancia de traslado o de transporte, a la unidad móvil, aérea, marítima o terrestre, destinada al
traslado de pacientes, cuya condición no sea de urgencia ni requiera de cuidados intensivos.</p>
<p>4.2. Ambulancia de urgencias básicas, a la unidad móvil, aérea, marítima o terrestre, destinada al servicio de
pacientes que requieren atención prehospitalaria de las urgencias médicas mediante soporte básico de vida.</p>
<p>4.3. Ambulancia de urgencias avanzadas, a la unidad móvil, aérea, marítima o terrestre, destinada al servicio
de pacientes que requieren atención prehospitalaria de las urgencias médicas mediante soporte avanzado de vida.</p>
<p>4.4. Ambulancia de cuidados intensivos, a la unidad móvil, aérea, marítima o terrestre, destinada al servicio
de pacientes que por su estado de gravedad requieren atención prehospitalaria de las urgencias médicas mediante
soporte avanzado de vida y cuidados críticos.</p>
<p>4.5. Atención médica, El conjunto de servicios que se proporcionan al individuo, con el fin de proteger,
promover y restaurar su salud.</p>
<p>4.6. Atención Prehospitalaria de las urgencias médicas, a la otorgada al paciente cuya condición clínica se
considera que pone en peligro la vida, un órgano o su función, con el fin de lograr la limitación del daño y su
estabilización orgánico funcional, desde el primer contacto hasta la llegada y entrega a un establecimiento para la
atención médica con servicio de urgencias.</p>
<p>4.7. Centro Regulador de Urgencias Médicas, la instancia técnico-médico-administrativa, responsabilidad de la
Secretaría de Salud Estatal o del Gobierno del Distrito Federal, en su caso, que establece la secuencia de las
actividades específicas para la atención prehospitalaria, en el sitio del evento crítico, el traslado y la recepción en el
establecimiento para la atención médica designado, con la finalidad de brindar atención médica oportuna y
especializada las 24 horas del día, los 365 días del año. El número de CRUM’s que deba haber en una entidad
federativa, estará determinado por las características geopoblacionales en forma local.</p>
<p>4.8. Número económico, número asignado a una unidad móvil para propósitos de identificación, que es
otorgado internamente por la Institución a la que pertenece.</p>
<p>4.9. Abreviaturas</p>
<p>4.9.1. CRUM, Centro Regulador de Urgencias Médicas.</p>
<p>4.9.2 TUM, Técnico en Urgencias Médicas o TEM, Técnico en Emergencias Médicas, son equivalentes para fines
de la presente norma, pueden tener un nivel de formación técnica básica o como técnico superior universitario.
