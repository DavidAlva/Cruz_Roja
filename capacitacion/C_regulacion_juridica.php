<h3>COMPETENCIA CONCEPTUAL</h3>
Competente para el manejo de los fundamentos teóricos-prácticos-filosóficos sobre la atención prehospitalaria
así como para evaluar las situaciones de riesgos propios, del ambiente y del paciente que pongan en peligro la vida,
un órgano o una función y que requiera de atención médica de emergencia o urgencia.

<h3>COMPETENCIA METODOLOGICA</h3>
Competente para aplicar el método clínico, científico, epidemiológico, educativo, documental y administrativo
para el manejo de la atención médica prehospitalaria protocolizada, así como para la realización de actividades de
administración, docencia e investigación en el área de la atención médica prehospitalaria de urgencia.

<h3>COMPETENCIA INTERPERSONAL</h3>
Competente para brindar atención prehospitalaria trabajando en equipo con compromiso y responsabilidad
social, de acuerdo a los valores éticos de la profesión, cuidando la integridad de los usuarios de acuerdo a los
estándares y procedimientos nacionales e internacionales vigentes bajo la dirección médica e interactuando con los
familiares y responsables legales.

<h3>COMPETENCIA CONTEXTUAL</h3>
Competente para brindar atención prehospitalaria afrontando las situaciones inherentes al contexto sociocultural
en el que sucede el evento; respetando costumbres y creencias, y adecuando el procedimiento vigente a las
necesidades de la población con la capacidad de resistir la presión social.

