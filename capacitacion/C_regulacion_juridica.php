<h3>1. Introducción</h3>
<p>La salud es factor de progreso y fuente de oportunidades para el bienestar individual y colectivo, así como un
medio eficaz para el desarrollo de las capacidades y potencialidades de las personas, que incide directamente en el
mejoramiento de la calidad de vida de los individuos y de la sociedad.</p>

<p>En la actualidad, toda persona está expuesta a sufrir un accidente o una enfermedad súbita que puede
amenazar su vida o su estado de salud. En México, desde hace algunas décadas, las enfermedades
cardiovasculares representan la primera causa de muerte entre la población general (la mitad de éstos son infartos);
las lesiones no intencionales, son la cuarta causa de mortalidad general (sin contar las lesiones con violencia y los
suicidios).</p>

<p>Sin embargo, es importante destacar que en las edades preescolar, escolar y en las etapas productivas de la
vida, las lesiones llegan a ser la primera causa de muerte y discapacidad.</p>

<p>La medicina moderna está dirigida a revertir el creciente número de decesos asociados a enfermedades graves
de aparición súbita o a accidentes con lesiones severas, llevando la atención fuera de los establecimientos médicos
para llegar y beneficiar directamente al paciente, por ello, la atención prehospitalaria debe concebirse como parte de
un proceso que se inicia desde el primer contacto, donde se le brinda el manejo inicial, ya sea en el hogar, vía
pública, sitio de recreación o en el ámbito laboral y continúa con la derivación a los establecimientos para la atención médica en donde proseguirá su atención con el tratamiento definitivo.</p>

<p>En nuestro País, el campo de las urgencias médicas prehospitalarias, no ha logrado alcanzar el desarrollo
suficiente para abatir los índices de morbilidad y mortalidad, en aquellas personas lesionadas o enfermas, que
requieren ser atendidas y tratadas con oportunidad y eficacia a fin de limitar el daño y tener mayores probabilidades
de sobrevivir con las menores secuelas posibles.</p>

<p>Debido a ello, se ha presentado la necesidad de contar con un modelo operativo, coordinado y sistematizado,
que garantice y asegure, en todos los casos, una atención médica prehospitalaria con niveles mínimos homogéneos
de calidad y seguridad, independientemente del prestador de servicios de que se trate, ya que la ausencia de un
marco jurídico específico en esta materia, ha propiciado que dicha atención sea heterogénea, con alto grado de
vulnerabilidad e incertidumbre sobre la oportunidad, calidad y seguridad en el tratamiento que le espera al paciente.</p>

<p>En este contexto, la Secretaría de Salud impulsa programas, mecanismos, estrategias y emite disposiciones
sanitarias, para hacer efectivo el derecho a la protección de la salud.</p>

<p>Dentro de las disposiciones sanitarias, la norma de atención prehospitalaria de las urgencias médicas, permite
contar con elementos regulatorios para que la prestación de servicios en unidades móviles tipo ambulancia, se lleve
a cabo con niveles homogéneos de calidad y seguridad en beneficio de la población en general.</p>

<p>Es pertinente señalar que las ambulancias de traslado por estar destinadas a realizar esta función
exclusivamente, no brindan atención prehospitalaria de las urgencias médicas, sin embargo, también quedan
sujetas a la regulación que establece la presente norma.</p>

