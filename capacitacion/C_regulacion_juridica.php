<h3>2. Objeto y campo de aplicación</h3>

<p>2.1. Esta Norma Oficial Mexicana tiene por objeto establecer los criterios mínimos que se deben observar en la
atención prehospitalaria de las urgencias médicas, los requisitos y características del personal involucrado, así como
el equipamiento e insumos mínimos para las unidades móviles tipo ambulancia.</p>

<p>2.2. Esta Norma Oficial Mexicana es de observancia obligatoria para todos los prestadores de servicios médicos,
de los sectores público, social y privado, que brinden traslado y atención prehospitalaria de las urgencias médicas,
excepto los destinados a los servicios de las fuerzas armadas en algunos numerales, por ordenamiento jurídico
específico.</p>

Quedan excluidas las unidades destinadas a la obtención de órganos y tejidos con fines terapéuticos.
