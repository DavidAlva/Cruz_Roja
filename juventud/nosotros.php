  <center>
  <h3>PRINCIPIOS FUNDAMENTALES </h3>
  <h3>del Movimiento Internacional de la Cruz Roja y de la Media Luna Roja</h3>
  
  <h6>Aprobados por la XX Conferencia Internacional de la Cruz Roja, Viena 1965 y
Revisados por la XXV Conferencia Internacional de la Cruz Roja y de la Media Luna Roja, Ginebra 1986.</h6>

<br>
<h3>Humanidad</h3>
<p>El Movimiento Internacional de la Cruz Roja y de la Media Luna Roja, al que ha dado nacimiento la preocupación de prestar auxilio sin discriminación, a todos los heridos en los campos de batalla, se esfuerza bajo su aspecto internacional y nacional, en prevenir y aliviar el sufrimiento de los hombres en todas circunstancias, tiende a proteger la vida y la salud, así como hacer respetar a la persona humana, favorece la comprensión mutua, la amistad, la cooperación y una paz duradera entre todos los pueblos.</p>
 
 <h3>Imparcialidad</h3>
 <p>No hace ninguna distinción de nacionalidad, raza, religión, condición social, ni credo político, se dedica únicamente a socorrer a los individuos en proporción con los sufrimientos, remediar sus necesidades y dando prioridad a las más urgentes.
</p>

<h3>Neutralidad</h3>
<p>Con el fin de conservar la confianza de todos, el Movimiento se abstiene de tomar parte en las hostilidades y, en todo tiempo, en las controversias de orden político, racial, religioso e ideológico. </p>

<h3>Independencia</h3>
<p>El Movimiento es independiente. Auxiliares de los poderes públicos en sus actividades humanitarias y sometidas a las leyes que rigen los países respectivos las Sociedades Nacionales deben, sin embargo, conservar una autonomía que les permita actuar siempre de acuerdo con los Principios del Movimiento.</p>
 
<h3>Voluntariado</h3>
<p>Es un movimiento de socorro voluntario y de carácter desinteresado. </p>

<h3>Unidad</h3>
<p>En cada país, sólo puede existir una sola Sociedad de la Cruz Roja o de la Media Luna Roja, debe ser accesible a todos y extender su acción humanitaria a la totalidad del territorio.</p>

<h3>Universalidad</h3>
<p>El Movimiento Internacional de la Cruz Roja y de la Media Luna Roja, en cuyo seno todas las sociedades tienen los mismos derechos y el deber de ayudarse mutuamente es universal.</p>
